#!/usr/bin/env python3
"""Run a real HunyuanImage3 request and build a MoE routing page."""

from __future__ import annotations

import argparse
import os
import shutil
import subprocess
import sys
from datetime import datetime, timezone
from pathlib import Path

DEFAULT_PROMPT = "生成一张复杂的咖啡图"
DEFAULT_MODEL = "tencent/HunyuanImage-3.0-Instruct"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("--model", default=DEFAULT_MODEL)
    parser.add_argument("--prompt", default=DEFAULT_PROMPT)
    parser.add_argument("--output-root", default=None)
    parser.add_argument("--height", type=int, default=1024)
    parser.add_argument("--width", type=int, default=1024)
    parser.add_argument("--steps", type=int, default=10)
    parser.add_argument("--guidance-scale", type=float, default=0.0)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--ar-devices", default="0,1")
    parser.add_argument("--dit-devices", default="2,3")
    parser.add_argument("--ar-tp", type=int, default=2)
    parser.add_argument("--dit-tp", type=int, default=2)
    parser.add_argument("--ar-gpu-memory-utilization", type=float, default=0.80)
    parser.add_argument("--ar-max-model-len", type=int, default=8192)
    parser.add_argument("--ar-max-num-batched-tokens", type=int, default=8192)
    parser.add_argument("--connector-port", type=int, default=50051)
    parser.add_argument("--init-timeout", type=int, default=900)
    parser.add_argument("--publish-dir", default=None)
    parser.add_argument("--rank-policy", choices=["rank0", "all"], default="rank0")
    return parser.parse_args()


def repo_root() -> Path:
    return Path(__file__).resolve().parents[3]


def repo_python(root: Path) -> str:
    venv_python = root / ".venv" / "bin" / "python"
    return str(venv_python) if venv_python.exists() else sys.executable


